#Sale de un dado el premio por completar la mision del juego
#1-gana un cofre/2-gana una gema/3-gana una vara/4-gana el premio mayor/5-no gana nada

valor = 64

if valor == 1:
    print("Ganó un cofre")
elif valor == 2:
    print("Ganó una gema")
elif valor == 3:
    print("Ganó una vara")
elif valor == 4:
    print ("Ganó el premio mayor")  
elif valor == 5:
    print ("No ganó nada")        
elif valor == 6:
    print("Ganó otra cosa")
else:
    print ("Valor inválido")    
